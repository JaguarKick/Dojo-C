#include <stdio.h>

int main()
{
	printf("Storage size of char		: %d\n",	sizeof(char));
	printf("Storage size of unsigned char	: %d\n",	sizeof(unsigned char));
	printf("Storage size of short		: %d\n",	sizeof(short));
	printf("Storage size of unsigned short	: %d\n",	sizeof(unsigned short));
	printf("Storage size of int 		: %d\n",	sizeof(int));
	printf("Storage size of unsigned int	: %d\n",	sizeof(unsigned int));
	printf("Storage size of long		: %d\n",	sizeof(long));
	printf("Storage size of unsigned long	: %d\n",	sizeof(unsigned long));
	printf("Storage size of float		: %d\n",	sizeof(float));
	printf("Storage size of double		: %d\n",	sizeof(double));
	return 0;
}
